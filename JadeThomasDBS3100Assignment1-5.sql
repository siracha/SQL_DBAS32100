SELECT s.carserial AS serial, 
c.carlistprice AS price, carmodel AS model,
sum(s.carsaleprice) OVER () 
AS "cumulative sale price totals"
FROM SI.saleinv s
JOIN SI.car c
ON c.carserial = s.carserial
WHERE c.carmake = 'JAGUAR'
;

